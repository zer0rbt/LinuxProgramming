#include <bits/stdc++.h>
using namespace std;

int main() {
        unsigned int a = UINT_MAX;
        cout << "a=" <<  a << '\n';
        int aa = static_cast<int>(a);
        cout << "aa=" << aa << '\n';
        int b = INT_MAX;
        cout << "b=" << b << '\n';
        unsigned int bb = static_cast<unsigned int>(b);
        cout << "bb=" << bb << '\n';
        return 0;
}
